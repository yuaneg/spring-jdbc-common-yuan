package com.jdbc.yuan.common.annotation;

/**
 * create by 袁恩光 2016-8-28
 */
public enum Operator {

	/**
	 * 小于
	 */
	LT,
	/**
	 * 大于
	 */
	GT,
	/**
	 * 等于
	 */
	EQ,
	/**
	 * 不等于
	 */
	NEQ,
	/**
	 * 小于等于
	 */
	LTE,
	/**
	 * 大于等于
	 */
	GTE,
	/**
	 * 模糊查询
	 */
	LIKE,
	/**
	 * 左侧
	 */
	LLIKE,
	/**
	 * 右侧模糊
	 */
	RLIKE,
	/**
	 * 双向模糊查询
	 */
	LRLIKE
	
}
